    Teatrul de Operă și Balet este una dintre cele mai frumoase clădiri din Odesa și unul dintre cele mai importante centre culturale din Ucraina. Inaugurat în 1887, teatrul impresionează prin arhitectura sa barocă și rococo și prin spectacolele de operă și balet de înaltă calitate.</p>

