            <p class="prs">1. Palatul Culturii
                Palatul Culturii este simbolul orașului Iași și una dintre cele mai impresionante clădiri din România. Construit între 1906 și 1925, palatul găzduiește patru muzee: Muzeul de Istorie, Muzeul Etnografic, Muzeul de Artă și Muzeul Științei și Tehnicii. Arhitectura sa neogotică este remarcabilă, iar vizitatorii pot explora diversele expoziții și colecții.</p>

<p class="prs">2. Catedrala Mitropolitană din Iași
Catedrala Mitropolitană este una dintre cele mai mari biserici ortodoxe din România și un important loc de pelerinaj, fiind locul unde se află moaștele Sfintei Parascheva. Construită între 1833 și 1839, catedrala impresionează prin dimensiunile sale și decorul interior bogat.</p>



<p class="prs">4. Grădina Botanică „Anastasie Fătu”
    Grădina Botanică din Iași, una dintre cele mai mari și vechi din România, oferă vizitatorilor ocazia de a explora diverse colecții de plante. Fondată în 1856 de către Anastasie Fătu, grădina include sectoare tematice precum grădina japoneză, sectorul de plante medicinale și serele cu plante exotice.</p>



